#del is used to delete a reference to an object. Any variable or list value can be deleted using del.
my_variable1 = 20
my_variable2 = "GeeksForGeeks"
 
# check if my_variable1 and my_variable2 exists
print(my_variable1)
print(my_variable2)
 
# delete both the variables
del my_variable1
del my_variable2
 
# check if my_variable1 and my_variable2 exists
print(my_variable1)
print(my_variable2)

